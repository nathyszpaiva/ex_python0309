# Tupla é uma estrutura de dados semelhante a lista. Porém, ela tem a característica de ser imutável,
# ou seja, após uma tupla ser criada, ela não pode ser alterada. Para criar uma lista com elementos,
# deve-se usar parênteses e adicionar os itens  entre eles separados por vírgula

alunos = ("Douglas", "Oscar", "Astarote", "Renato", "Paulo")
print(alunos)
print(alunos[4])

